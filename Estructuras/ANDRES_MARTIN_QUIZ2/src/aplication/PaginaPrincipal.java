    private void printMezclaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printMezclaButtonActionPerformed
        String texto = "";
        textAreaBox.setText(texto);
        if (Global.mezcla.esVacio()){
            JOptionPane.showMessageDialog(null,"No hay mezcla.");
        }else{
            texto = Global.mezcla.preorden();
        }
        textAreaBox.setText(texto);
        
    }//GEN-LAST:event_printMezclaButtonActionPerformed

    private void printRechazadosButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printRechazadosButtonActionPerformed
        Funciones f = new Funciones();
        String texto = "";
        textAreaBox.setText(texto);
        if (Global.valoresRechazados.esVacio()){
            JOptionPane.showMessageDialog(null, "Todavia no ha habido ningun valor rechazado.");
        }else{
            texto = f.leerRechazados();
        }
        textAreaBox.setText(texto);
               
    }//GEN-LAST:event_printRechazadosButtonActionPerformed

