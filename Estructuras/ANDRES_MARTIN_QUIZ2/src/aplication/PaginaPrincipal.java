    private void crearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearButtonActionPerformed
        String tamano = tamanoBox.getText();
        Funciones f = new Funciones();
        try{
            int tamanoI = Integer.parseInt(tamano);
            if (tamanoI > 0 && tamanoI < 10){
            f.agregarArbol(tamanoI);
            }else{
                JOptionPane.showMessageDialog(null, "Ocurrio un error: Ingrese valores menores de 1000 y positivos"  );
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Ocurrio un error: \n" + e);
        }
    }//GEN-LAST:event_crearButtonActionPerformed

