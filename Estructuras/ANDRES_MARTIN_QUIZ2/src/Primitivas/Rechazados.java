package Primitivas;

/**
 *
 * @author Andres
 */
public class Rechazados {
    
    private int dato;
    private int arbol;

    public Rechazados(int dato,int arbol) {
        this.dato = dato;
        this.arbol = arbol;
    }

    public Rechazados() {
    }

    /**
     * @return the dato
     */
    public int getDato() {
        return dato;
    }

    /**
     * @param dato the dato to set
     */
    public void setDato(int dato) {
        this.dato = dato;
    }

    /**
     * @return the arbol
     */
    public int getArbol() {
        return arbol;
    }

    /**
     * @param arbol the arbol to set
     */
    public void setArbol(int arbol) {
        this.arbol = arbol;
    }
    
    
    
    
    
}
